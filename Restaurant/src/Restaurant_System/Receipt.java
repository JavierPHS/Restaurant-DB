    public Receipt(String[] item, int[] quantity, float[] value, int counter, boolean discount) {
        initComponents();
        this.setLocationRelativeTo(null);
        float price;
        String formatPrice;
        String formatTotal;
        PreparedStatement ps;
        float total = 0;
        //calculate price
        for (int i = 0; i < counter; i++) {
            price = quantity[i] * value[i];
            formatPrice = String.format("%.02f", price);
            listText.append(quantity[i] + " " + item[i] + "\t\t" + formatPrice + "\n");
            total += price;
        }
        if (discount) {
            total -= 5;
            if (total <= 0) {
                total = 0;
            }
        }
        formatTotal = String.format("%.02f", total);
        listText.append("\n\n---------------------------------------------------\nTotal Price : \t\t" + formatTotal);
        listText.append("\n\n Thank you for ordering from Johnny's Burgers!");
    }
